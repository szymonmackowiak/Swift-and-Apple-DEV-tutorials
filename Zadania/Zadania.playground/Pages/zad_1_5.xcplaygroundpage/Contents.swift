/* Zadanie 1.5
Za pomocą polecenia type(of: ) sprawdź typ następujących obiektów:
 - 5
 - 6.5
 - 5.0
 - "5"
 
 Dlaczego w pierwszym, trzecim i ostatnim przypadku dostajemy różne typy?
 */

print("Obiekt 5 ma typ \(type(of: 5))")
print("Obiekt 6.5 ma typ \(type(of: 6.5))")
print("Obiekt 5.0 ma typ \(type(of: 5.0))")
print("Obiekt '5' ma typ \(type(of: "5"))")
